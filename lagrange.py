    polinomio.append([f0, f1, f2, f3])
    if n == 1:
        l1 = ((f0)*((x - x1) / (x0 - x1)) + ((f1)*((x - x0) / (x1 - x0))))
        polinomio.append([l1])
    elif n == 2:
        l1 = ((f0)*((x - x1)*(x - x2)) / ((x0 - x1)*(x0 - x2)) + (f1)*((x - x0)*(x - x2)) / ((x1 - x0)(x1 - x2)) + (f2)*((x - x0)*(x - x1)) / ((x2 - x0)*(x2 - x1)))
