        l1 = ((f0)*((x - x1)*(x - x2)*(x - x3))/((x0 - x1)*(x0 - x2)*(x0 - x3)) + (f1)*((x - x0)*(x - x2)*(x - x3)) / ((x1 - x0)*(x1 - x2)*(x1 - x3)) + (f2)*((x - x0)*(x - x1)*(x - x3)) / ((x2 - x0)*(x2 - x1)*(x2 - x3)) + (f3)*((x - x0)*(x - x1)*(x - x2)) / ((x3 - x0)*(x3 - x0)*(x3 - x1)*(x3 - x2)))
